course = 'Python for Beginners'
#         0123456789...
# This is how python strings are indexed

# course[0] will return P
print(course[0])

print(course[-1])
# a negative index will return characters from the end of the string
# course[-1] returns s

print(course[0:3])
# print characters from index 0 to index 3, but not index 3

print(course[1:])
# will print from the 1st index to the end of the word
# will print ython for Beginners

print(course[:5])
# will print from 0th index to 5th index

print(course[:])
# will assume 0 and the start index and the the last index.




